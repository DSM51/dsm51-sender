require 'serialport'

$port = SerialPort.new '/dev/ttyUSB0', 57600, 8, 1, SerialPort::NONE


class DSM
  attr_reader :port

  def initialize(port)
    @port = port
  end

  def send(*bytes)
    payload = bytes + [crc(bytes)]
    puts "send: #{payload.map { |x| x.to_s(16) }}"
    port.write payload.map(&:chr).join
  end

  def recv(size)
    size.times.map { port.read(1).ord }
  end

  def crc(bytes)
    256 - bytes.inject(0) { |s,i| (s+i)%256 }
  end


  def ping
    send 0x54
    recv(1).first == 0x43
  end

  def op
    send 0x51
    sleep 0.15
    recv(1).first == 0x06
  end

  def code
    send 0x50, 0x00, 0x00, 0x00
    sleep 0.15
    recv(1).first == 0x06
  end

  def restart
    send 0x52
    recv(1).first == 0x06
  end
end



